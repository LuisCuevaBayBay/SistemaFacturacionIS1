    void mostrardatos(String valor){
        ConexionSQL cc = new ConexionSQL();
        Connection cn = cc.getConnection();
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("ID Empleado");
        modelo.addColumn("Nombre Empleado");
        modelo.addColumn("Apellido Empleado");
        modelo.addColumn("Numero de Identidad Empleado");
        modelo.addColumn("Direccion Empleado");
       
        
        tablaempleados.setModel(modelo);
        String sql = "";
        if(valor.equals("")){
            sql= "SELECT * From vendedor";
        }
        else{
            sql="SELECT * FROM vendedor WHERE (vendedor_id='"+valor+"' OR nombre_empleado='"+valor+"' OR apellido_empleado='"+valor+"'OR num_identidad_empleado='"+valor+"'OR direccion='"+valor+"'OR usuario='"+"'OR pass='"+valor+"')";
            }
        String[] datos = new String[7];
        try{
            Statement st = cn.createStatement();
            ResultSet rs = st.executeQuery(sql);
            while(rs.next()){
                datos[0] = rs.getString(1);
                datos[1] = rs.getString(2);
                datos[2] = rs.getString(3);
                datos[3] = rs.getString(4);
                datos[4] = rs.getString(5);
                datos[5] = rs.getString(6);
                datos[6] = rs.getString(7);
                
                modelo.addRow(datos);
                
            }    
        }catch(SQLException ex){
               
        }
        }
